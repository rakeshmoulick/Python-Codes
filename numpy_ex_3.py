# operations on arrays
import numpy as np
a = np.array([0.0, 2.0, 4.0, 8.0])

print("\nPrinting a/8:")
print(a/8.0)

print("\nPrinting (a-4.0):")
print(a - 4.0)

print("\nPrinting sqrt(a):")
print(np.sqrt(a))





